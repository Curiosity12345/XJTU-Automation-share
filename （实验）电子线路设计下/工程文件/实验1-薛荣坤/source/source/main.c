void Port_IO_Init()
{
	//set P4[1] to push_pull model
    P74OUT |= 0x01;
	//enable crossbar
    XBR2 |= 0x40;
    //close the alam P4.1
    P4 &= 0xfd;
	//uart1,int1
	XBR1 |= 0x14;
	XBR2 |= 0x44;
}

void Oscillator_Init()
{
    int i = 0;
    OSCXCN    = 0x67;
    for (i = 0; i < 3000; i++);  // Wait 1ms for initialization
    while ((OSCXCN & 0x80) == 0);
    OSCICN    = 0x08;
}

void Timer0_Init(void)
{
	
	TH0 = TIMER0_RELOAD_HIGH;           // Reinit Timer0 High register
	TL0 = TIMER0_RELOAD_LOW;
	ET0 = 1;                            // Timer0 interrupt enabled
	TMOD = 0x01;                        // 16-bit Mode Timer0
	//TCON |= 0x10;                        // Timer0 ON
}


void Interrupt_Init()
{
	//INT1
	//low level triggle
	IT1 = 0;
	//enable INT1
	EX1 = 1;
	//enable all interrupt
	EA = 1;
}

void Init_Device(void)
{
    //disable watchdog
    EA = 0;
    WDTCN = 0xde;
    WDTCN = 0xad;
    EA = 1;
	  //SYSCLK_Init();
    Oscillator_Init();
    Port_IO_Init();
	
		Interrupt_Init();
	
    LcdInit();
		LedInit();
}

void TimeCounter(int second){
	
	while(Time_num<second*100){
		TR0=1;
		LedDispNum();
		if(answerf==1){
			time+=Time_num;
			break;
		}
	}
	TR0=0;
	time+=Time_num;
	Time_num=0;
}

void ResDis(void){
	while(Time_num<5*100){
		TR0=1;
		ResDispNum();
	}
	TR0=0;
	Time_num=0;
}

